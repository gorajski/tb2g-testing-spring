@SpringJUnitWebConfig(locations = {"classpath:spring/mvc-test-config.xml", "classpath:spring/mvc-core-config.xml"})
class OwnerControllerTest {

    @Autowired
    private OwnerController ownerController;

    private MockMvc mockMvc;

    @BeforeEach
    void setUp() {
        mockMvc = MockMvcBuilders.standaloneSetup(ownerController).build();
    }

    @Test
    void testNewOwnerPostValid() throws Exception {
        mockMvc.perform(post("/owners/new")
                .param("firstName", "Jimmy")
                .param("lastName", "Kimmel")
                .param("Address", "123 Hollywood Blvd")
                .param("City", "Los Angeles")
                .param("telephone", "2223334444")   //Each of these params are tagged @NotEmpty in the model
                                                                   //so each is required to be present for the test to pass.
        ).andExpect(status().is3xxRedirection());

    }
}